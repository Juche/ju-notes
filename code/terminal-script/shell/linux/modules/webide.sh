#!/bin/bash
set -e

WEBIDE_WORKSPACE=$WORKSPACE/webide
WEBIDE_TAR=$PACKAGE_DIR/webide.tar.gz

cd $PACKAGE_DIR
echo "开始安装webide"
####################################################################################################################################

echo "[1]创建webide工作空间"
mkdir -p $WEBIDE_WORKSPACE

####################################################################################################################################

echo "[2]开始解压..."
tar -zxf $WEBIDE_TAR -C $WEBIDE_WORKSPACE

####################################################################################################################################

echo "[3]脚本结束"

####################################################################################################################################

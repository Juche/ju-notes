#!/bin/bash
set -e

VSO_DIR=$WORKSPACE/vscode-online
VSO_TAR=vscode-online.tar.gz

cd $PACKAGE_DIR
echo "开始安装vscode-online"
####################################################################################################################################

echo "[1]创建vscode-online"
mkdir -p $VSO_DIR

####################################################################################################################################

echo "[2]开始解压..."
tar -zxf $VSO_TAR -C $VSO_DIR

####################################################################################################################################

echo "[3]安装完成"

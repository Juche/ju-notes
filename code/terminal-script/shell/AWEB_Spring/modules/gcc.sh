#!/bin/bash
set -e 

ENV_PACEAGES=$PACKAGE_DIR/envPackages
GCC_DIR=$ENV_PACEAGES/gcc

####################################################################################################################################

cd $ENV_PACEAGES
if [ -e $GCC_DIR/gcc ];then
echo "[1/3]已存在gcc文件夹"
else
echo "[1/3]解压gcc.tar.gz压缩包"
tar -zxf gcc.tar.gz 
fi

####################################################################################################################################

cd $GCC_DIR
echo "[2/3]安装依赖"
rpm -ivh  *.rpm --force --nodeps

####################################################################################################################################

echo "[3/3]脚本结束"

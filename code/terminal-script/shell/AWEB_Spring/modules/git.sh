#!/bin/bash                                                                                                                                                                               
set -e 

GIT_TAR=git.tar.gz
GIT_DIR=$PACKAGE_DIR/git

####################################################################################################################################

if [ -e $GIT_DIR ];then
echo "清理文件"
rm -rf $GIT_DIR
fi

####################################################################################################################################

cd $PACKAGE_DIR
echo "[1/4]解压压缩包"
tar -zxf $GIT_TAR 

cd $GIT_DIR
echo "[2/4]开始安装"
rpm -ivh ./*.rpm --force --nodeps

####################################################################################################################################

echo "[3/4]清理文件"
rm -rf $GIT_DIR 

####################################################################################################################################

echo "[4/4]脚本结束"


